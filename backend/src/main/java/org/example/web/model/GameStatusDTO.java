package org.example.web.model;

/**
 * Перечисление статусов игры для веб-интерфейса (API).
 * <p>
 * Эти значения передаются клиенту в формате JSON и используются
 * для отображения результата игры на стороне пользователя.
 */
public enum GameStatusDTO {

    /** Игра продолжается. Клиент должен разрешить пользователю делать ходы. */
    PLAYING,

    /** Игра завершена. Победили крестики (обычно это игрок-человек). */
    CROSS_WIN,

    /** Игра завершена. Победили нолики (обычно это ИИ). */
    ZERO_WIN,

    /** Игра завершена. Свободных клеток нет, победитель не выявлен. */
    DRAW
}