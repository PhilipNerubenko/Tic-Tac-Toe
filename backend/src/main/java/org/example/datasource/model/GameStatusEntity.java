package org.example.datasource.model;

/**
 * Перечисление возможных состояний игровой сессии в слое данных.
 * <p>
 * Используется для фиксации текущего этапа игры и определения победителя
 * при сохранении в хранилище.
 */
public enum GameStatusEntity {

    /** Игра в процессе: игроки продолжают совершать ходы */
    PLAYING,

    /** Игра завершена: победа игрока, играющего крестиками (X) */
    CROSS_WIN,

    /** Игра завершена: победа игрока, играющего ноликами (0) */
    ZERO_WIN,

    /** Игра завершена: ничья (поле заполнено, победитель не выявлен) */
    DRAW
}