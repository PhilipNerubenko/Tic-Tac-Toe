package org.example.domain.model;

/**
 * Представляет содержимое ячейки игрового поля.
 * <p>
 * Каждому типу ячейки соответствует целочисленное значение,
 * используемое при сериализации и хранении в слое данных.
 */
public enum CellType {

    /** Свободная ячейка, в которую можно совершить ход */
    EMPTY(0),

    /** Ячейка, занятая крестиком (Первый игрок) */
    CROSS(1),

    /** Ячейка, занятая ноликом (Второй игрок) */
    ZERO(2);

    /** Числовое представление типа ячейки для хранения в БД/массивах */
    private final int value;

    /**
     * Конструктор типа ячейки.
     * @param value внутреннее числовое значение.
     */
    CellType(int value) {
        this.value = value;
    }

    /**
     * Возвращает числовой код ячейки.
     * @return 0 — пусто, 1 — крестик, 2 — нолик.
     */
    public int getValue() {
        return value;
    }
}