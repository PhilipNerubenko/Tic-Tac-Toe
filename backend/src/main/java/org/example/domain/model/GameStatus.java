package org.example.domain.model;

/**
 * Статус игровой сессии в бизнес-логике приложения.
 * <p>
 * Отражает текущее состояние игрового процесса и фиксирует финальный результат.
 */
public enum GameStatus {

    /** Активная стадия игры: игроки могут совершать ходы. */
    PLAYING,

    /** Завершение игры: победа игрока, ставившего крестики (X). */
    CROSS_WIN,

    /** Завершение игры: победа игрока, ставившего нолики (0). */
    ZERO_WIN,

    /** Завершение игры: ничья (свободных ячеек не осталось, победитель не определен). */
    DRAW
}